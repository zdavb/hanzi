# 清理和去重函数
def clean_and_deduplicate(text):
    # 移除空白字符，只保留汉字
    import re
    chars = re.findall(r'[\u4e00-\u9fa5]', text)
    unique_chars = []
    seen = set()
    for char in chars:
        if char not in seen:
            unique_chars.append(char)
            seen.add(char)
    return unique_chars

